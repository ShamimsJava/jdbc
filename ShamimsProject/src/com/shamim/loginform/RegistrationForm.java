    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        
        if(txtFirstName.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Insert First Name.");
            txtFirstName.requestFocus();
        }else if(txtLastName.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Insert Last Name.");
            txtLastName.requestFocus();
        }else if(!(Pattern.matches("^[a-zA-Z0-9]+[@]{1}+[a-zA-Z0-9]+[.]{1}+[a-zA-Z0-9]+$", txtEmail.getText()))){
            JOptionPane.showMessageDialog(null, "Insert Your Valid Email.");
            txtEmail.requestFocus();
        }else if(txtPassword.getPassword().length<3){
            JOptionPane.showMessageDialog(null, "Insert Password Minimum 4 Characters.");
            txtPassword.requestFocus();
        }else if(txtConfirmPassword.getPassword().toString().equals(txtPassword.getPassword().toString())){
            JOptionPane.showMessageDialog(null, "Do Not Match The Password");
            txtConfirmPassword.requestFocus();
        }else{
        String firstName = txtFirstName.getText();
        String lastName = txtLastName.getText();
        String email = txtEmail.getText();
        String userName = txtUser.getText();
        String pass = txtPassword.getText();
        String password = txtConfirmPassword.getText();
        
        UserClass uc = new UserClass(firstName, lastName, email, userName, password);
        UserService us = new UserService();
        us.insert(uc);
        lblMessage.setText("All information are saved.");
        }
        
        
    }//GEN-LAST:event_btnSubmitActionPerformed

