    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String userName = txtUser.getText();
        String password = txtPassword.getText();
        UserService us = new UserService();
        try {
            UserClass uc = us.getLogin(userName);
            if (uc.getUserName().equals(userName) && uc.getPassword().equals(password)) {
                this.setVisible(false);
                new AddressBook().setVisible(true);
            } else {
                lblMessage.setText("Wrong Password.");
            }
        } catch (Exception e) {
            lblMessage.setText("This username is not available.");
        }
    }//GEN-LAST:event_btnLoginActionPerformed

