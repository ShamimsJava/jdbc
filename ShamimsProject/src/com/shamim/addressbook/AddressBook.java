    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        Address ad = new Address(txtFirstName.getText(), txtLastName.getText(), txtEmail.getText(), txtMobile.getText(), txtAddress.getText());
        as.insert(ad);
        lblMessage.setText("Value inserted successfully.");
    }//GEN-LAST:event_btnInsertActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        try {
            AddressService as = new AddressService();
            ResultSet rs =  as.search(txtSearch.getText());
            
            while(rs.next()){
                txtFirstName.setText(rs.getString(1));
                txtLastName.setText(rs.getString(2));
                txtEmail.setText(rs.getString(3));
                txtMobile.setText(rs.getString(4));
                txtAddress.setText(rs.getString(5));
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnSearchActionPerformed

