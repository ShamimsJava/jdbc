    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        studentService = new StudentService();
        studentService.insert(new Students(Integer.parseInt(txtId.getText()), txtName.getText()));
        lblMessage.setText("1 record successfully inserted.");
        clear();
    }//GEN-LAST:event_btnInsertActionPerformed

    private void btnDisplayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDisplayActionPerformed
        refreashTable();
    }//GEN-LAST:event_btnDisplayActionPerformed

    private void tblDispalyMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDispalyMouseClicked
        int i = tblDispaly.getSelectedRow();
        TableModel model = tblDispaly.getModel();
        txtId.setText(model.getValueAt(i, 0).toString());
        txtName.setText(model.getValueAt(i, 1).toString());
    }//GEN-LAST:event_tblDispalyMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        studentService = new StudentService();
        studentService.update(new Students(Integer.parseInt(txtId.getText()), txtName.getText()));
        lblMessage.setText("1 record successfully updated.");
        clear();
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        studentService = new StudentService();
        int i = tblDispaly.getSelectedRow();
        TableModel model = tblDispaly.getModel();
        int id = Integer.parseInt(model.getValueAt(i, 0).toString());
        studentService.delete(id);
        lblMessage.setText("1 record successfully deleted.");
        clear();
    }//GEN-LAST:event_btnDeleteActionPerformed

    public void refreashTable() {
        DefaultTableModel model = (DefaultTableModel) tblDispaly.getModel();
        model.setRowCount(0);
        displayStudentsWithinTable();
    }

    public void displayStudentsWithinTable() {
        studentService = new StudentService();
        List<Students> students = studentService.getStudents();
        DefaultTableModel model = (DefaultTableModel) tblDispaly.getModel();
        Object[] col = new Object[2];
        for (int i = 0; i < students.size(); i++) {
            col[0] = students.get(i).id;
            col[1] = students.get(i).studentName;
            model.addRow(col);
        }
    }

    private void clear() {
        txtId.setText("");
        txtName.setText("");
    }

