    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        studentService = new StudentService();
        Student student = new Student(Integer.parseInt(txtId.getText()), txtName.getText());
        studentService.save(student);
        lblMessage.setText("1 Record Successfully Saved!");
        refreshTable();
        clearFormAfterAction();
    }//GEN-LAST:event_btnAddActionPerformed

    private void tblDisplayMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDisplayMouseClicked
        int i = tblDisplay.getSelectedRow();
        TableModel model = tblDisplay.getModel();
        txtId.setText(model.getValueAt(i, 0).toString());
        txtName.setText(model.getValueAt(i, 1).toString());
    }//GEN-LAST:event_tblDisplayMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        studentService = new StudentService();
        Student student = new Student(Integer.parseInt(txtId.getText()), txtName.getText());
        studentService.update(student);
        lblMessage.setText("1 Record Successfully Updated!");
        refreshTable();
        clearFormAfterAction();
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        studentService = new StudentService();
        int i = tblDisplay.getSelectedRow();
        TableModel model = tblDisplay.getModel();
        int id = Integer.parseInt(model.getValueAt(i, 0).toString());
        studentService.delete(id);
        lblMessage.setText("1 Record Successfully Deleted!");
        refreshTable();
        clearFormAfterAction();
    }//GEN-LAST:event_btnDeleteActionPerformed

    public void refreshTable() {
        DefaultTableModel model = (DefaultTableModel) tblDisplay.getModel();
        model.setRowCount(0);
        displayStudentsWithinTable();
    }

    public void displayStudentsWithinTable() {
        studentService = new StudentService();
        List<Student> students = studentService.getStudents();
        DefaultTableModel model = (DefaultTableModel) tblDisplay.getModel();
        Object[] col = new Object[2];

        for (int i = 0; i < students.size(); i++) {
            col[0] = students.get(i).getId();
            col[1] = students.get(i).getStudentName();
            model.addRow(col);
        }
    }

    public void clearFormAfterAction() {
        txtId.setText("");
        txtName.setText("");
    }

